package HerenciaAplicada;

public class Cliente {
	private String nombre;
	private String Nit;
	
	
	public Cliente(String nombre, String nit) {
		super();
		this.nombre = nombre;
		Nit = nit;
	}
	public String getNombre() {
		return nombre;
	}
	public String getNit() {
		return Nit;
	}
	
	
	
	
	

}
